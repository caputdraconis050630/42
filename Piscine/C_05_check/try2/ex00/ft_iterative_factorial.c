int	ft_iterative_factorial(int nb)
{
	int	i;
	int	ans;

	ans = 1;
	i = 1;
	if (nb < 0)
		return (0);
	while (i <= nb)
	{
		ans *= i;
		i += 1;
	}
	return (ans);
}
